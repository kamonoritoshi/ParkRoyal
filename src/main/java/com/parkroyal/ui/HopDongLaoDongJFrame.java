    int row = -1;
    HopDongLaoDongDAO dao = new HopDongLaoDongDAO();
    
    void init() {
        setLocationRelativeTo(null);
        this.fillTable();
        this.row = -1;
        this.updateStatus();
    }
    
    void fillTable() {
        DefaultTableModel model = (DefaultTableModel) tblHopDongLaoDong.getModel();
        model.setRowCount(0);
        try {
            List<HopDongLaoDong> list = dao.select();
            for (HopDongLaoDong hdld : list) {
                Object[] row = {
                    hdld.getMaCV(),
                    hdld.getMaNV(),
                    DateHelper.toString(hdld.getNgayKy(), "dd/MM/yyyy"),
                    DateHelper.toString(hdld.getNgayBDLV(), "dd/MM/yyyy")
                };
                model.addRow(row);
            }
        } catch (Exception e) {
            DialogHelper.alert(this, "Lỗi truy vấn dữ liệu!");
        }
    }
    
    void insert() {
        HopDongLaoDong model = getForm();
        try {
            dao.insert(model);
            this.fillTable();
            this.clearForm();
            DialogHelper.alert(this, "Thêm mới thành công!");
        } catch (Exception e) {
            DialogHelper.alert(this, "Thêm mới thất bại!");
        }
    }
    
    void update() {
        HopDongLaoDong model = getForm();
        try {
            dao.update(model);
            this.fillTable();
            DialogHelper.alert(this, "Cập nhật thành công!");
        } catch (Exception e) {
            DialogHelper.alert(this, "Cập nhật thất bại!");
        }
    }
    
    void clearForm() {
        cboNhanVien.setSelectedIndex(0);
        cboChucVu.setSelectedIndex(0);
        txtNgayKy.setText("");
        txtNgayBDLV.setText("");
        txtPhongBan.setText("");
        this.row = -1;
        this.updateStatus();
    }
    
    void edit() {
        Integer MaCV = (Integer) tblHopDongLaoDong.getValueAt(this.row, 0);
        Integer MaNV = (Integer) tblHopDongLaoDong.getValueAt(this.row, 1);
        HopDongLaoDong hdld = dao.findById(MaCV, MaNV);
        this.setForm(hdld);
        this.updateStatus();
        tabs.setSelectedIndex(0);
    }
    
    NhanVienDAO nvdao = new NhanVienDAO();
    ChucVuDAO cvdao = new ChucVuDAO();
    PhongBanDAO pbdao = new PhongBanDAO();
    
    void setForm(HopDongLaoDong hdld) {
        cboNhanVien.setSelectedIndex(hdld.getMaNV() - 1);
        cboChucVu.setSelectedIndex(hdld.getMaCV() - 1);
        txtNgayKy.setText(DateHelper.toString(hdld.getNgayKy(), "dd/MM/yyyy"));
        txtNgayBDLV.setText(DateHelper.toString(hdld.getNgayBDLV(), "dd/MM/yyyy"));
        txtPhongBan.setText(pbdao.findById(nvdao.findById(hdld.getMaNV()).getMaPB()).getTenPB());
    }
    
    HopDongLaoDong getForm() {
        HopDongLaoDong hdld = new HopDongLaoDong();
        hdld.setNgayKy(DateHelper.toDate(txtNgayKy.getText(), "dd/MM/yyyy"));
        hdld.setNgayBDLV(DateHelper.toDate(txtNgayBDLV.getText(), "dd/MM/yyyy"));
        hdld.setMaCV(cboChucVu.getSelectedIndex() + 1);
        hdld.setMaNV(cboNhanVien.getSelectedIndex() + 1);
        return hdld;
    }
    
    void first() {
        this.row = 0;
        this.edit();
    }
    void prev() {
        if (this.row > 0) {
            this.row--;
            this.edit();
        }
    }
    void next() {
        if (this.row < tblHopDongLaoDong.getRowCount() - 1) {
            this.row++;
            this.edit();
        }
    }
    void last() {
        this.row = tblHopDongLaoDong.getRowCount() - 1;
        this.edit();
    }
    
    void updateStatus() {
        boolean edit = (this.row >= 0);
        boolean first = (this.row == 0);
        boolean last = (this.row == tblHopDongLaoDong.getRowCount() - 1);
        // Trạng thái form
        btnThem.setEnabled(!edit);
        btnSua.setEnabled(edit);
        btnMoi.setEnabled(edit);
        // Trạng thái điều hướng
        btnFirst.setEnabled(edit && !first);
        btnPrev.setEnabled(edit && !first);
        btnNext.setEnabled(edit && !last);
        btnLast.setEnabled(edit && !last);
    }
    
    void fillComboBoxNV() {
        DefaultComboBoxModel model = (DefaultComboBoxModel) cboNhanVien.getModel();
        model.removeAllElements();
        try {
            List<NhanVien> list = nvdao.select();
            for (NhanVien nv : list) {
                model.addElement(nv);
            }
        } catch (Exception e) {
            DialogHelper.alert(this, "Lỗi truy vấn nhân viên!");
        }
    }
    
    void fillComboBoxCV() {
        DefaultComboBoxModel model = (DefaultComboBoxModel) cboChucVu.getModel();
        model.removeAllElements();
        try {
            List<ChucVu> list = cvdao.select();
            for (ChucVu cv : list) {
                model.addElement(cv);
            }
        } catch (Exception e) {
            DialogHelper.alert(this, "Lỗi truy vấn chức vụ!");
        }
    }
    
